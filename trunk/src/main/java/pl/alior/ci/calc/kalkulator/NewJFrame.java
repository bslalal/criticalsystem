    double lastValue;
    boolean isLastValue = false;
    int NONE = 0;
    int DODAWANIE = 1;
    int ODEJMOWANIE = 2;
    int DZIELENIE = 3;
    int MNOZENIE = 4;
    int operation = NONE;
    Addition addition = new Addition();
    Division division = new Division();
    Multiplication multiplication = new Multiplication();
    Subtraction subtraction = new Subtraction();
    
    private void doCalculation() {

        double result = Double.parseDouble(jTextField2.getText());
                jTextField2.setText("");
        if (isLastValue) {
            if (operation == DODAWANIE) {
                result = addition.calculate(lastValue, result);
            }
            if (operation == DZIELENIE) {
                result = division.calculate(lastValue, result);
            }
            if (operation == MNOZENIE) {
                result = multiplication.calculate(lastValue, result);
            }
            if (operation == ODEJMOWANIE) {
                result = subtraction.calculate(lastValue, result);
            }
            operation = NONE;
            jTextField2.setText("" + result);
        }
        lastValue = result;
        isLastValue = true;

    }

